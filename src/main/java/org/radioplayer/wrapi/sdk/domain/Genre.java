package org.radioplayer.wrapi.sdk.domain;

public class Genre {
    private String href;
    private String name;
    private String type;

    public String getHref() {
        return href;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(String type) {
        this.type = type;
    }
}
